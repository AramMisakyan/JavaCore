package javaBookExample.chapters.chapter2;

public class ifSample {
    public static void main(String[] args) {
        int x, y;
        x = 100;
        y = 50;
        if (x > y) {
            System.out.println("x մեծ է y");
        }
        if (x < y) {
            System.out.println("x փոքր է y");
        }
        if (x == y) {
            System.out.println("x հտվասար է y");
        }
    }
}
